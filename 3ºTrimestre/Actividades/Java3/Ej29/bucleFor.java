public class bucleFor{


	
	public static void main (String [] args){

	
		for (int i = 1; i <=10; i++){
			
		System.out.println("El numero actual es ; " + i);		
	
		if(i ==5){
		
		System.out.println("Saltando el 5... ");
		continue;		
	
		}
		
		if (i == 8){

		System.out.println("Se detecta 8, saliendo...");
		break;		
	
		}
	
		}
	

	}








}